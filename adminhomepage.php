<script>
    const citySelect = document.getElementById("city");
    const startDateInput = document.getElementById("start-date");
    const endDateInput = document.getElementById("end-date");
    const startTimeInput = document.getElementById("top-start-time");
    const endTimeInput = document.getElementById("top-end-time");
    const rentButton = document.getElementById("rent-button");
    const rentalDetails = document.getElementById("rental-details");
    const modal = document.getElementById("myModal");
    const closeBtn = document.querySelector(".close");
    const cityOptions = document.querySelectorAll(".city-option");
    
    // Function to set the date input fields to the current date
    function setCurrentDate() {
        const today = new Date();
        const yyyy = today.getFullYear();
        const mm = String(today.getMonth() + 1).padStart(2, '0');
        const dd = String(today.getDate()).padStart(2, '0');
        const todayString = `${yyyy}-${mm}-${dd}`;
        startDateInput.value = todayString;
        endDateInput.value = todayString;
    }
    
    // Show the modal when the city dropdown is clicked
    citySelect.addEventListener("click", () => {
        setCurrentDate(); // Set the date fields to the current date
        modal.style.display = "block";
    });
    
    // Close the modal when the close button is clicked
    closeBtn.addEventListener("click", () => {
        modal.style.display = "none";
    });
    
    // Set the selected city when a city option is clicked
    cityOptions.forEach((option) => {
        option.addEventListener("click", () => {
            const selectedCity = option.getAttribute("data-city");
            citySelect.value = selectedCity; // Update the form field
            modal.style.display = "none";
        });
    });
    
    // Close the modal if the user clicks outside of it
    window.addEventListener("click", (event) => {
        if (event.target === modal) {
            modal.style.display = "none";
        }
    });
    
    // Event listener for the "Rent Now" button
    <!-- Event listener for the "Rent Now" button in index.php -->
rentButton.addEventListener("click", () => {
    const selectedCity = citySelect.value;
    const startDate = startDateInput.value;
    const startTime = startTimeInput.value;
    const endDate = endDateInput.value;
    const endTime = endTimeInput.value;

    if (selectedCity && startDate && startTime && endDate && endTime) {
        // Construct the URL with query parameters
        const url = `rentals.php?city=${selectedCity}&start_date=${startDate}&start_time=${startTime}&end_date=${endDate}&end_time=${endTime}`;
        window.location.href = url; // Redirect to trial.php
    } else {
        alert("Please fill in all fields before renting.");
    }
});

    
    
    </script>
    

    
